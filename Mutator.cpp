float Mutator::dist(string reference, string probe) {
	long long distance = 0;
	long long maxpossible = 0;
	std::ifstream source(reference, std::ios_base::binary);
	std::ifstream mutation(probe, std::ios_base::binary);
	while (source) {
		std::ios::pos_type before = source.tellg();
		unsigned char x;
		unsigned char y;
		source >> x;
		mutation >> y;
		std::ios::pos_type after = source.tellg();
		distance += Popcnt8[static_cast<int>(x) & static_cast<int>(y)];
		maxpossible += 8;
	}
	return float(distance/maxpossible);
}